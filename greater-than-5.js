// compare both first and second input numbers to 5
// if both numbers are greater than or equal to 5 then print "true"
// if any of 2 input numbers are less than 5 then print "false"

// set first input variable
let num1 = 5;
// set second input variable
let num2 = 6;

// if else statement:
